#include "aux.h"

int	is_alpha(char *str)
{
	int	b;

	b = 1;
	while (*str)
	{
		if (!((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z')))
			b = 0;
		str++;
	}
	return (b);
}
